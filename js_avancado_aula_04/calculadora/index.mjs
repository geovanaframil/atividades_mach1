export function dividir(valor1, valor2) {
  return valor1 / valor2;
}
export function multiplicar(valor1, valor2) {
  return valor1 * valor2;
}
export function somar(valor1, valor2) {
  return valor1 + valor2;
}
export function subtrair(valor1, valor2) {
  return valor1 - valor2;
}
export default {
  multiplicar,
  dividir,
  somar,
  subtrair,
};
